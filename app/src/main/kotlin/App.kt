package pl.umamusume.yhkcatprint.app

import pl.umamusume.yhkcatprint.bluetooth.*

suspend fun main() {
    val bluetoothManager = BluetoothManager(when {
        System.getProperty("os.name").startsWith("Windows") -> Win32NativeGateway
        else -> throw UnsupportedOperationException("Unsupported OS")
    })
    println("Bluetooth Manager initialized")

    val adapters = bluetoothManager.listAdapters()
    println("Found ${adapters.size} Bluetooth adapters")

    val adapter = adapters.firstOrNull()
        ?: throw IllegalStateException("No Bluetooth adapters found")
    println("Using adapter: ${adapter.name} [${adapter.address}]")

    val devices = adapter.getPairedDevices()
    println("Found ${devices.size} paired devices")

    val device = devices.firstOrNull { it.address == "24:00:28:00:1e:5b" }
        ?: throw IllegalStateException("Target device not found among paired devices")
    println("Using device: ${device.name} [${device.address}]")

    val socket = device.createSocket(2)
    println("Created RFCOMM socket")

    try {
        socket.connect()
        println("Socket connected")

        socket.close()
        println("Socket closed after connection")
    } finally {
        socket.close()
        println("Socket closed")
    }
}

